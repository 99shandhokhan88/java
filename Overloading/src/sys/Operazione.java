package sys;

public class Operazione {
	
	public int somma(int a, int b) {
		
		int somma = 0;
		
		somma = (a + b);
		
		
		return (somma);
		
	}
	
	public int somma(int a, int b, int c) {
		
		int somma = 0;
		
		somma = (a + b + c);
		
		
		return (somma);
		
	}
	
	public float somma(float a, float b) {
		
		float somma = 0;
		
		somma = (a + b);
		
		
		return (somma);
		
	}
	
public float somma(float a, float b, float c) {
		
		float somma = 0;
		
		somma = (a + b + c);
		
		
		return (somma);
		
	}

}
